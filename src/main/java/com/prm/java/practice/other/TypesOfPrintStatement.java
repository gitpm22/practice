package com.prm.java.practice.other;

public class TypesOfPrintStatement {

	public static void main(String[] args) {
		
		System.out.println("Preeti");
		System.out.println("Mandavgade");
		
		System.out.print("Preeti");
		System.out.print("Mandavgade" + "\n");
		
		System.out.println("Preeti" + "Mandavgade");
		
		float value = 3.234444566f;
		
		System.out.printf("%.4f", value);

	}

}
