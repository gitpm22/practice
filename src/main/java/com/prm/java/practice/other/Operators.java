package com.prm.java.practice.other;

public class Operators {

	public static void main(String[] args) {
		
		// Unary Operators
		
		int a = 1;
		System.out.println(a);
		int x = a++ + 10;
		System.out.println(a);
		
		a=x;
		
  		System.out.println(a);
		
		int b = 1;
		
		b = ++b + 1;
		
 		System.out.println(b);

 		
	}

}
